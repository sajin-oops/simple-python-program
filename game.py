# This program adds two numbers provided by the user

# Get input from the user
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

# Add the two numbers
sum = num1 + num2

# Display the result
print("The sum of {0} and {1} is {2}".format(num1, num2, sum))

